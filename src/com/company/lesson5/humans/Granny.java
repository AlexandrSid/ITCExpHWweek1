package com.company.lesson5.humans;

public class Granny extends Human{
    @Override
    public void fastRun() {
        System.out.println("When I was 20 I ran fast");
    }

    @Override
    public void slowRun() {
        System.out.println("Hurry, the bus will be in a minute");
    }

    @Override
    public void restSwim() {
        System.out.println("I'm not staying on the bottom");
    }

    @Override
    public void resultSwim() {
        System.out.println("Do WHAT? You want me die?");
    }
}
